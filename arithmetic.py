def add(num1, num2):
	return num1 + num2

def subtract(num1, num2):
	return num1 - num2

def multiply(num1, num2):
	return num1 * num2

def divide(num1, num2):
	return num1 / num2

def square(num1):
	return pow(num1, 2)

def cube(num1):
	return pow(num1, 3)

def mod(num1, num2):
	return num1 % num2

def power(num1, num2):
	return pow(num1, num2)